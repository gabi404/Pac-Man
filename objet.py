def affiche_coin(map, screen, player, tuch_coin):
    # affiche les coins et les pac-gommes et les supprimes quand ils sont touchés par pac-man
    tuch = 0
    compt = 0
    fin = 0
    for i in range (0,28):
        for j in range (0,31):
            if 2<=map[j][i]<3:           
                coin_rect = screen.blit(coin.convert_alpha(),(23*i+221,23*j+bordure))
                if player.rect.colliderect(coin_rect):
                    screen.blit(coin_black.convert_alpha(),(23*i+221,23*j+bordure))
                    player.score += 10
                    map[j][i] = 0
                    sound_coin = pygame.mixer.Sound('assets/sound/pac_eat_coin.wav').play() 
                    sound_coin.set_volume(0.5)
            elif map[j][i] == 3:
                JACKPOT_rect = screen.blit(JACKPOT.convert_alpha(), (23*i+216,23*j+bordure-5))
                if player.rect.colliderect(JACKPOT_rect):
                    screen.blit(JACKPOT_black.convert_alpha(),(23*i+216,23*j+bordure))
                    player.score += 50
                    map[j][i] = 0
                    tuch = 2
                    sound_jackpot = pygame.mixer.Sound('assets/sound/pac_eat_jackpot.wav').play()
                    sound_jackpot.set_volume(0.5)
                    return tuch
            if map == map2:
                fin = 3
                return fin

